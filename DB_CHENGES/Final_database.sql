--  DDL for View VW_BUDGET_USAGE
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ADMIN"."VW_BUDGET_USAGE" ("FULL_NAME", "CATEGORY", "MONTHLY_LIMIT", "SPENT_AMOUNT", "USAGE_PERCENTAGE") DEFAULT COLLATION "USING_NLS_COMP"  AS 
  SELECT
    u.full_name,
    bc.name AS category,
    bc.monthly_limit,
    bc.spent_amount,
    ROUND((bc.spent_amount / bc.monthly_limit) * 100, 2) AS usage_percentage
FROM budget_categories bc
JOIN users u ON bc.user_id = u.id
;
