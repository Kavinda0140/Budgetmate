--  DDL for View VW_USER_TRANSACTION_SUMMARY
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ADMIN"."VW_USER_TRANSACTION_SUMMARY" ("USER_ID", "FULL_NAME", "DESCRIPTION", "CATEGORY", "AMOUNT", "TYPE", "TRANSACTION_DATE") DEFAULT COLLATION "USING_NLS_COMP"  AS 
  SELECT
    u.id AS user_id,
    u.full_name,
    t.description,
    t.category,
    t.amount,
    t.type,
    t.transaction_date
FROM users u
JOIN transactions t ON u.id = t.user_id
;
